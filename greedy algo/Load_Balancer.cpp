void solve(){
   ll n;
   ll a[9000];
   while(1){
    cin>>n;
    if(n==-1)break;
    ll load=0;
    rep(i,n){
        cin>>a[i];
        load+=a[i];
    }
    if(load%n!=0){
        cout<<-1<<endl;
        continue;
    }
    load=load/n;
    ll max_load=0;
    ll diff=0;
    rep(i,n){
        diff+=(a[i]-load);
        ll ans=max(diff,-diff);
        max_load=max(max_load,ans);
    }
    cout<<max_load<<endl;

   }
}

